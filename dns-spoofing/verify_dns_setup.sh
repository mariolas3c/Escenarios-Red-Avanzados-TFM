echo "curl http://10.0.0.80"
echo "curl http://10.0.0.99"
echo ""
echo "# Acceder por nombre (requiere DNS funcionando):"
echo "curl http://www.banco.com"
echo ""
echo "# Forzar uso de DNS especifico:"
echo "host www.banco.com 10.0.0.53"
